// ─── Claude call (direct in prototype; should be proxied in production) ──────
async function callClaude(messages, system, apiKey) {
  if (!apiKey || !apiKey.startsWith('sk-ant-')) {
    // Mock mode
    await new Promise(r => setTimeout(r, 2200));
    return JSON.stringify(MOCK_ANALYSIS);
  }

  /**
   * PRODUCTION NOTE:
   * Replace this fetch with a call to your own backend:
   *   POST /api/analyze  →  { messages, system }
   * The backend stores ANTHROPIC_API_KEY in .env and proxies to Anthropic.
   * This avoids exposing the key in the browser.
   */
  const res = await fetch('http://localhost:3000/api/analyze', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      model: 'claude-sonnet-4-5',
      max_tokens: 2000,
      system: system || '',
      messages,
    }),
  });

  if (!res.ok) {
    const e = await res.json();
    throw new Error(e.error?.message || 'API Error ' + res.status);
  }

  const d = await res.json();
  const raw = d.content.map(c => c.text || '').join('');
  const cleaned = raw.replace(/```json\s*/g, '').replace(/```\s*/g, '').trim();
  console.log('Claude cleaned output:', cleaned);
  return cleaned;
}

