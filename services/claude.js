// ─── Safe JSON parser ────────────────────────────────────────────────────────
function safeParseAnalysis(raw) {
  try {
    const cleaned = raw
      .replace(/```json|```/g, '')
      .trim();
    const match = cleaned.match(/\{[\s\S]*\}/);
    if (match) return JSON.parse(match[0]);
    return JSON.parse(cleaned);
  } catch {
    return {
      overview: raw,
      risks: '—',
      valuation: '—',
      buyers: '—',
      dd: '—',
    };
  }
}

